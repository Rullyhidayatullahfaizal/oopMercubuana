    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        if(txtkodematakuliah.getText().equals("")){
            JOptionPane.showMessageDialog(this, "silahkan data yang akan dihapus !");
        }else{
            int jawab = JOptionPane.showConfirmDialog(null, "data ini akan dihapus,lanjutkan ?","konfirmasi",JOptionPane.YES_NO_CANCEL_OPTION);
            if (jawab == 0) {
                try {
                    st = cn.createStatement();
                    String sql = "DELETE FROM mata_kuliah WHERE kode_mata_kuliah = '" + txtkodematakuliah.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"data berhasil disimpan ?");
                    TampilData();
                    Bersih();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
         try {
            st = cn.createStatement();
            String selectedJurusan = txtjurusan.getSelectedItem().toString();

            if (selectedJurusan.equals("") ||
                    txtkodematakuliah.getText().equals("") ||
                    txtmatakuliah.getText().equals("") ||
                    txtsks.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "data tidak boleh kosong ","validasi data",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            //simpan data
            if (btnsimpan.getText() == "simpan") {
                String cek = "SELECT * FROM mata_kuliah WHERE kode_mata_kuliah = '" + txtkodematakuliah.getText() + "'";
                rs = st.executeQuery(cek);
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "upps data ini sudah ada");
                }else{
                    String sql = "INSERT INTO mata_kuliah VALUES ('" + selectedJurusan +
                            "','" + txtkodematakuliah.getText() +
                            "','" + txtmatakuliah.getText() +
                            "','" + txtsks.getText() + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"data berhasil disimpan");
                    Bersih();
                    TampilData();
                }
            }else{
                //aksi ubah data
                String update = "UPDATE mata_kuliah SET jurusan ='" + selectedJurusan +
                        "', mata_kuliah = '" + txtmatakuliah.getText()+
                        "', sks = '" + txtsks.getText() +
                        "' WHERE kode_mata_kuliah = '" + txtkodematakuliah.getText() + "'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null,"data berhasil disimpan");
                Bersih();
                TampilData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

